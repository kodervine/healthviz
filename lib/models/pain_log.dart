class PainLog {
  final double painLevel;
  final String painLocation;
  final String painDuration;
  final String painType;
  final String notes;
  final DateTime date;

  PainLog({
    required this.painLevel,
    required this.painLocation,
    required this.painDuration,
    required this.painType,
    required this.notes,
    required this.date,
  });
}
